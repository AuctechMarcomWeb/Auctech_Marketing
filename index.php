   let recordId = null;

$(document).ready(function () {
    $('#phone, #name').on('input', function () {
        const phone = $('#phone').val().trim();
        const name = $('#name').val().trim();

        if (phone.length < 10 || name === '') {
            return;
        }

        $.post('save_query.php', {
            name: name,
            phone: phone,
            mode: 'auto',
            id: recordId
        }, function (res) {
            console.log('Auto-save response:', res);

            if (res.startsWith('success|')) {
                const id = res.split('|')[1];
                $('#record_id').val(id);
                recordId = id;
            }
        });
    });

    $('#enqForm').submit(function (e) {
        e.preventDefault();

        const phone = $('#phone').val().trim();
        const name = $('#name').val().trim();
        const id = $('#record_id').val();

        $.post('save_query.php', {
            name: name,
            phone: phone,
            mode: 'manual',
            id: id
        }, function (res) {
            if (res.startsWith('success|')) {
                Swal.fire({
                    icon: 'success',
                    title: 'Thank you!',
                    text: 'Your query has been sent successfully.'
                });

                $('#enqForm')[0].reset();
                $('#record_id').val('');
                recordId = null;
            } else {
                Swal.fire({
                    icon: 'error',
                    title: 'Oops!',
                    text: res
                });
            }
        });
    });
});

</script>


