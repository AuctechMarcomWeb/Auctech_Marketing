        $(document).ready(function () {
            $('#enquiryForm').submit(function (e) {
                e.preventDefault();

                
                var phone = $('input[name="phone"]').val().trim();
                var phonePattern = /^[0-9]{10}$/;
                if (!phonePattern.test(phone)) {
                    Swal.fire({
                        icon: 'warning',
                        title: 'Invalid Phone Number',
                        text: 'Please enter a valid 10-digit phone number.'
                    });
                    return false;
                }

              
                var fileInput = $('input[name="image_path"]')[0];
                if (fileInput.files.length > 0) {
                    var file = fileInput.files[0];
                    var maxSize = 3 * 1024 * 1024;
                    if (file.size > maxSize) {
                        Swal.fire({
                            icon: 'warning',
                            title: 'File Too Large',
                            text: 'File size should not exceed 3 MB.'
                        });
                        return false;
                    }
                }

                var formData = new FormData(this);

                $.ajax({
                    url: 'admin/apply_job.php',
                    type: 'POST',
                    data: formData,
                    processData: false,
                    contentType: false,
                    success: function (response) {
                        console.log(response);
                        if (response == 'success') {
                            Swal.fire({
                                icon: 'success',
                                title: 'Application Received!',
                                text: 'Thank you for applying. Our team will contact you soon.',
                            }).then(function () {
                                $('#enquiryForm')[0].reset();
                            });
                        } else {
                            Swal.fire({
                                icon: 'error',
                                title: 'Oops...',
                                text: 'Invalid file type. Please upload a PDF, DOC, or DOCX.',
                            });
                        }
                    },
                    error: function (xhr, status, error) {
                        console.error("AJAX Error: ", status, error);
                        Swal.fire({
                            icon: 'error',
                            title: 'Oops...',
                            text: 'Something went wrong. Please try again later.',
                        });
                    }
                });
            });
        });s
    </script>

